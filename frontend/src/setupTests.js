// jest.setup.js
import fetchMock from 'jest-fetch-mock';

fetchMock.enableMocks();

// âœ… Mock localStorage globally
global.localStorage = {
  store: {},
  getItem(key) {
    return this.store[key] || null;
  },
  setItem(key, value) {
    this.store[key] = value.toString();
  },
  removeItem(key) {
    delete this.store[key];
  },
  clear() {
    this.store = {};
  }
};
